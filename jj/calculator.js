// ADDITION
const add = (num3, num4) => (num3 + num4);
console.log(add(4, 5));

// SUBSTRACTION
const sub = (num3, num4) => (num3 - num4);
console.log(sub(14, 5));

// MULTIPLICATION
const mul = (num3, num4) => (num3 * num4);
console.log(mul(4, 5));

// DIVISION
const divide = (num3, num4) => (num3/num4);
console.log(divide(9, 3));
